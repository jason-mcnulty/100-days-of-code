
# range(start, stop, step)
# For Loops: The loop will run a spacific number of times.
print ("My name is")
for i in range (5):
    print ("Jimmy File Times " + str(i))


# Because this for loop is using a range argument, you can set the range using a comma.
print ("range one through five")
for i in range (1, 5): # this will start and 1 and end at 4, not 5. 
    print ("Jimmy File Times " + str(i))


# You can add in a step argument. This will 
print ("range one through five")
for i in range (0, 102, 2): # this will start and 1 and end at 4, not 5. The last number is how the counting will step. 
    print ("Jimmy File Times " + str(i))